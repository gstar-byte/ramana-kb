def fix_page(num):
    fpath = os.path.join(qa_dir, f'qa-{num}.html')
    with open(fpath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    questions = page_questions.get(num, [])
    if not questions:
        return False
    
    # 找到所有 qa-q 并替换
    pattern = r'(<div class="qa-q">)❓ [^<]+(</div>)'
    
    def replacer(match):
        replacer.counter += 1
        idx = (replacer.counter - 1) % len(questions)
        return f'{match.group(1)}❓ {questions[idx]}{match.group(2)}'
    
    replacer.counter = 0
    new_content = re.sub(pattern, replacer, content)
    
    with open(fpath, 'w', encoding='utf-8') as f:
        f.write(new_content)
    
    return replacer.counter

# 执行修复
print("修复 qa-29 到 qa-43 的问题文本...\n")
for num in range(29, 44):
    count = fix_page(num)
    print(f"  ✓ qa-{num}.html: 修复了 {count} 个问题")

