for fp in files:
    with open(fp, encoding='utf-8') as f:
        content = f.read()

    # 获取当前title
    m_title = re.search(r'<title>(.*?)</title>', content, re.DOTALL)
    current_title = m_title.group(1).strip() if m_title else ''

    # 获取/生成description
    desc = get_desc(fp)
    if not desc:
        # 从title生成
        desc = get_desc_from_title(fp, current_title)

    desc = fix_desc(desc)

    original = content
    # 替换 meta description
    content = re.sub(
        r'<meta name=["\']description["\'][^>]*>',
        f'<meta name="description" content="{desc}">',
        content
    )
    # 同步 og:description
    content = re.sub(
        r'<meta (?:property|name)=["\']og:description["\'][^>]*>',
        f'<meta property="og:description" content="{desc}">',
        content
    )
    # 同步 twitter:description
    content = re.sub(
        r'<meta (?:property|name)=["\']twitter:description["\'][^>]*>',
        f'<meta name="twitter:description" content="{desc}">',
        content
    )

    if content != original:
        with open(fp, 'w', encoding='utf-8') as f:
            f.write(content)
        changed += 1

