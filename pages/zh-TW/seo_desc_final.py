def get_desc(fp):
    """根据文件路径获取目标description"""
    fname = os.path.basename(fp).replace('.html', '')
    rel = fp.replace('\\', '/')

    # 书籍详情页
    for book_key, desc in BOOK_DESCS.items():
        if rel == f'books/{book_key}.html':
            return desc

    # 书籍章节页
    for book_key, desc in BOOK_DESCS.items():
        if book_key in ['be-as-you-are', 'gems', 'back-to-heart', 'talks', 'crumbs', 'reflections']:
            # 章节数
            ch_map = {
                'be-as-you-are': 9, 'gems': 13, 'back-to-heart': 5,
                'talks': 8, 'crumbs': 4, 'reflections': 2,
            }
            ch_count = ch_map.get(book_key, 0)
            # 提取章节号
            m = re.search(r'-ch(\d+)\.html', fname)
            if m and fname.startswith(book_key):
                ch = int(m.group(1))
                return make_chapter_desc(book_key, ch)

    # 概念页
    if rel.startswith('concepts/'):
        key = fname
        if key in CONCEPT_DESCS:
            return CONCEPT_DESCS[key]
        return CONCEPT_DESCS.get('_template')

    # QA页
    for qa_key, desc in QA_DESCS.items():
        if rel == f'qa/{qa_key}.html':
            return desc
    if rel == 'qa/index.html':
        return '拉玛那马哈希精选问答150则，涵盖自我参究、心智本质、修行方法、上师、臣服、静默、解脱等核心主题，助您深入理解这位伟大灵性导师的教示精髓与修行智慧。'

    # 方法页
    if rel.startswith('methods/'):
        method = fname
        return f'{method.replace("-", " ").title()}是拉玛那马哈希教示中的重要修行方法。本页提供详细的方法说明、实践指引与经典引用，助您深入修行，提升灵性觉醒的层次与深度。'

    # 人物页
    if rel.startswith('persons/'):
        return '本页提供室利·拉玛那·马哈希的完整传记与灵性事迹，涵盖其出生、觉醒、阿鲁那佳拉山、静修院生活及核心教示。了解这位伟大印度灵性导师的生平与智慧遗产，为您的修行提供榜样指引。'

    # 首页
    if fp in ['index.html', 'index']:
        return '拉玛那马哈希知识库系统整理其灵性教示，包含18本经典著作完整导读、30+核心概念详解、150条精选问答、修行方法指南与关键人物传记，全面覆盖自我参究与静默之道的智慧体系。'

    # 知识图谱
    if fp == 'graph.html':
        return '拉玛那马哈希核心概念知识图谱，可视化展示真我、心智、自我参究、上师恩典等概念之间的关系，涵盖本体论、心智模型、修行法门与世界本质的完整知识网络，助您建立系统认知。'

    # 默认
    return None


def fix_desc(text, max_len=155):
    """截断超长description"""
    if len(text) > max_len:
        for sep in '。！？，、：；':
            pos = text.rfind(sep)
            if pos > 135:
                return text[:pos+1]
        return text[:154] + '…'
    return text


# 处理所有文件
for fp in files:
    with open(fp, encoding='utf-8') as f:
        content = f.read()

    desc = get_desc(fp)
    if not desc:
        continue

    desc = fix_desc(desc)
    original = content

    # 替换 meta description
    content = re.sub(
        r'<meta name=["\']description["\'][^>]*>',
        f'<meta name="description" content="{desc}">',
        content
    )

    # 同步 og:description 和 twitter:description
    for pat in ['og:description', 'twitter:description']:
        content = re.sub(
            r'<meta (?:property|name)=["\']' + pat + r'["\'][^>]*>',
            f'<meta property="{pat}" content="{desc}">',
            content
        )

    if content != original:
        with open(fp, 'w', encoding='utf-8') as f:
            f.write(content)
        changed += 1

print(f'Description fix: {changed}/{len(files)} files')
